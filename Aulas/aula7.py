# números e underline _
# O sinal de = é o operador de atribuião. Ele é usado para
# atribuir um valor a um nome (Variavel)
# Uso: nome_variavel = expressão
nome_completo = 'Hugo Rodrigues Guerra'
soma_dois_mais_dois = 2 + 2 
